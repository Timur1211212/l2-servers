// SEO текст для страницы сервера
function renderServerSeoText(server) {
    return `
        <div class="seo-content server-seo">
            <h2>О сервере ${escapeHtml(server.name)}</h2>
            <p>${escapeHtml(server.name)} — это ${server.status === 'VIP' ? 'премиальный' : 'качественный'} сервер Lineage 2 на хрониках ${server.version || 'Interlude'}. 
            ${server.description ? escapeHtml(server.description) : `Сервер предлагает рейты: Exp ${server.exp || 'x1'}, Drop ${server.drop || 'x1'}, Adena ${server.adena || 'x1'}.`}</p>
            
            <h3>Почему стоит играть на ${escapeHtml(server.name)}?</h3>
            <p>Игроки выбирают ${escapeHtml(server.name)} за ${getServerAdvantages(server)}. Сервер имеет рейтинг ${server.rating?.average || 0}/5 на основе ${server.rating?.count || 0} отзывов реальных игроков.</p>
            
            <h3>Как начать играть на ${escapeHtml(server.name)}?</h3>
            <ol>
                <li>Перейдите на официальный сайт сервера: <a href="${escapeHtml(server.website)}" target="_blank">${escapeHtml(server.website)}</a></li>
                <li>Зарегистрируйте аккаунт (логин и пароль)</li>
                <li>Скачайте игровой клиент ${server.version || 'Interlude'}</li>
                <li>Установите патч с сайта сервера</li>
                <li>Запустите игру и создайте персонажа</li>
            </ol>
            
            <h3>Какой класс выбрать на ${escapeHtml(server.name)}?</h3>
            <p>На сервере ${escapeHtml(server.name)} популярны следующие классы: Hawkeye (лучник) для PvP, Spoiler (сборщик) для фарма материалов, Bishop (хилер) для групп. Выбор класса зависит от вашего стиля игры.</p>
        </div>
    `;
}

function getServerAdvantages(server) {
    const advantages = [];
    if (server.status === 'VIP') advantages.push('высокую стабильность и профессиональную администрацию');
    if (server.exp && parseInt(server.exp) >= 100) advantages.push('быструю прокачку');
    if (server.exp && parseInt(server.exp) <= 10) advantages.push('хардкорный геймплей и ценность каждого предмета');
    if (server.openingDate && new Date(server.openingDate) > new Date(Date.now() - 30 * 24 * 60 * 60 * 1000)) {
        advantages.push('свежий старт и равные условия для всех');
    }
    
    if (advantages.length === 0) return 'стабильную работу и активное комьюнити';
    return advantages.join(', ');
}

